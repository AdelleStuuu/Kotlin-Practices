fun main() {
    println("\nHello! Enter your name here! ");
    // this makes it so that it reads the line below this print
    var name = readln();
    println("Nice! Enter your age here! ");
    var age = readln();

    // the print allows multiple inline calling for variables, kinda convenient
    println("\nYour name is $name and you are $age years old!\n");
}